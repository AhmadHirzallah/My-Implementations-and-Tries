#include "libft.h"
#include <string.h>

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	if (!(little[i]))
		return ((char *)&big[i]);
	while ((i < len) && (big[i]))
	{
		j = 0;
		while ((big[i + j]) && (big[i + j] == little[j]) && (i + j) < len)
		{
			if (!little[j + 1])
				return ((char *)&big[i]);
			j++;
		}
		i++;
	}
	return (0);
}
